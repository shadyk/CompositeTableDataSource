import UIKit

extension UIColor {
    static func customGray() -> UIColor {
        return UIColor(named: "GrayColor")!
    }
    
    static func customLightGray() -> UIColor {
        return UIColor(red: 222/255, green: 222/255, blue: 222/255, alpha: 0.5)
    }
    
    static func customGreen() -> UIColor {
        return UIColor(named: "GreenColor")!
    }
    
    static func customBlack() -> UIColor {
        return UIColor(named: "BlackColor")!
    }
    static func customWhite() -> UIColor {
        return UIColor(named: "WhiteColor")!
    }
    static func transparentBlack() -> UIColor {
        return UIColor(red: 32/255, green: 32/255, blue: 32/255, alpha: 0.5)
    }
}
